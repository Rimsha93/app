export const getUniversitiesByCategory = (category: string) => {
  return universities.filter(u => u.category === category);
};

export const getUniversityById = (id: string) => {
  return universities.find(u => u.id === id);
};

export const filterUniversities = (
  countries: string[],
  budget: string,
  fieldOfStudy: string
) => {
  let filtered = [...universities];
  
  if (countries.length > 0) {
    filtered = filtered.filter(u => countries.includes(u.country));
  }
  
  if (budget) {
    const budgetMap: Record<string, number> = {
      'under-20k': 20000,
      '20k-40k': 40000,
      '40k-60k': 60000,
      '60k-80k': 80000,
      'above-80k': 100000
    };
    const maxBudget = budgetMap[budget] || 100000;
    filtered = filtered.filter(u => u.totalCost <= maxBudget);
  }
  
  if (fieldOfStudy) {
    filtered = filtered.filter(u => 
      u.programs.some(p => p.toLowerCase().includes(fieldOfStudy.toLowerCase()))
    );
  }
  
  return filtered;
};
